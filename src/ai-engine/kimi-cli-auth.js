/**
 * Kimi CLI authentication bootstrap.
 *
 * WHY THIS EXISTS: the Kimi CLI does NOT read an API key from the environment.
 * It is built for a human at a terminal — `kimi login` runs a device-code flow
 * and writes credentials into <KIMI_CODE_HOME>/config.toml, and from then on the
 * CLI reads that file. On a container there is no human and no browser, so the
 * CLI finds no credentials and dies with "No model configured" before it writes
 * a single line of game code — even when MOONSHOT_API_KEY is set.
 *
 * The fix: skip the login flow and write that config file ourselves. Login has
 * no secret ingredient; the binary's own API-key path says as much
 * ("Your key will be saved to ~/.kimi-code/config.toml").
 *
 * The schema below was verified empirically against `kimi doctor config <path>`
 * (Kimi's own strict validator — it rejects unknown enum values, type-checks
 * every field, and requires models.*.max_context_size).
 */

import fs from 'fs';
import path from 'path';
import os from 'os';
import { getNvidiaTextKeys, nextNvidiaTextApiKey } from './nvidia-key-pool.js';

/** Where the CLI looks for its config. KIMI_CODE_HOME wins, else ~/.kimi-code. */
export function resolveKimiHome(env = process.env) {
    return env.KIMI_CODE_HOME || path.join(env.HOME || os.homedir(), '.kimi-code');
}

/**
 * Build config.toml text for the available credentials.
 *
 * Two platforms, two shapes — they are NOT interchangeable:
 *   - Moonshot (api.moonshot.ai)  → OpenAI-compatible, so type = "openai"
 *   - Kimi Code (api.kimi.com)    → native, so type = "kimi"
 * Valid types: anthropic | openai | kimi | google-genai | openai_responses | vertexai
 */
export function buildKimiConfigToml({ apiKey, baseUrl, model, providerType, contextSize = 262144 }) {
    // The models table key must be the EXACT model name, quoted — the CLI looks
    // up `[models."<default_model>"]` verbatim. Sanitizing the dot out of
    // "kimi-k2.7" produces: 'Model "kimi-k2.7" is not configured in config.toml'.
    const modelKey = `"${model}"`;
    return `# Generated by gametok — do not edit by hand.
# The Kimi CLI cannot read API keys from the environment, so we write the
# credentials it expects instead of running the interactive login flow.
default_model = "${model}"

[providers.gametok]
type = "${providerType}"
api_key = "${apiKey}"
base_url = "${baseUrl}"

[models.${modelKey}]
provider = "gametok"
model = "${model}"
max_context_size = ${contextSize}
`;
}

/**
 * Build the settings object for one provider, or null if its key is missing.
 * `provider`:
 *   - 'moonshot'  → api.moonshot.ai (the intended primary, once paid)
 *   - 'nvidia'    → integrate.api.nvidia.com hosted Kimi K2.6 (fallback)
 *   - 'kimi-code' → api.kimi.com native (last resort)
 * Each result carries a `.provider` tag so callers can log/branch on it.
 */
function buildProviderSettings(provider, env = process.env) {
    if (provider === 'moonshot') {
        const key = String(env.MOONSHOT_API_KEY || '').trim();
        if (!key) return null;
        return {
            provider: 'moonshot',
            apiKey: key,
            baseUrl: String(env.MOONSHOT_BASE_URL || 'https://api.moonshot.ai/v1').replace(/\/+$/, ''),
            model: String(env.MOONSHOT_MODEL || 'kimi-k2.7').trim(),
            providerType: 'openai', // Moonshot's API is OpenAI-compatible
            contextSize: Number(env.MOONSHOT_MAX_CONTEXT || 262144),
        };
    }
    if (provider === 'nvidia') {
        // NVIDIA API catalog hosts Kimi K2.6, OpenAI-compatible.
        // Slug + 256K context verified against docs.api.nvidia.com (moonshotai-kimi-k2-6).
        const key = nextNvidiaTextApiKey(env) || getNvidiaTextKeys(env)[0] || '';
        if (!key) return null;
        return {
            provider: 'nvidia',
            apiKey: key,
            baseUrl: String(env.NVIDIA_BASE_URL || 'https://integrate.api.nvidia.com/v1').replace(/\/+$/, ''),
            model: String(env.NVIDIA_MODEL || 'moonshotai/kimi-k2.6').trim(),
            providerType: 'openai', // NVIDIA's catalog is OpenAI-compatible
            contextSize: Number(env.NVIDIA_MAX_CONTEXT || 262144),
        };
    }
    if (provider === 'kimi-code') {
        const key = String(env.KIMI_CODE_API_KEY || env.KIMI_API_KEY || '').trim();
        if (!key) return null;
        return {
            provider: 'kimi-code',
            apiKey: key,
            baseUrl: String(env.KIMI_CODE_BASE_URL || 'https://api.kimi.com/coding/v1').replace(/\/+$/, ''),
            model: String(env.MOONSHOT_MODEL || 'kimi-k2.7').trim(),
            providerType: 'kimi',
            contextSize: Number(env.KIMI_CODE_MAX_CONTEXT || 262144),
        };
    }
    return null;
}

/**
 * Pick the provider settings for a given preference.
 *
 * Precedence (preference 'auto'): Moonshot is the intended primary; NVIDIA's
 * hosted Kimi is the fallback under it; KIMI_CODE is a last-resort native fallback.
 * NOTE: absence-based — a *broken* Moonshot key still wins here (its presence is
 * all we can see). The runtime fail-over that recovers from a broken key lives in
 * the runner (maker-kimi-cli-runner.js): it detects a billing/auth failure and
 * re-runs with preference 'nvidia'.
 *
 * @param {string} preference 'auto' | 'moonshot' | 'nvidia' | 'kimi-code'
 * @returns {object|null} settings with a `.provider` tag, or null if unavailable
 */
export function resolveKimiSettings(env = process.env, preference = 'auto') {
    if (preference !== 'auto') return buildProviderSettings(preference, env);
    return (
        buildProviderSettings('moonshot', env) ||
        buildProviderSettings('nvidia', env) ||
        buildProviderSettings('kimi-code', env)
    );
}

/**
 * Which providers have a key configured. This is a PEEK — it must not advance
 * the NVIDIA key rotation (buildProviderSettings does, via nextNvidiaTextApiKey),
 * or every availability probe would silently burn a key.
 */
export function availableKimiProviders(env = process.env) {
    const out = [];
    if (String(env.MOONSHOT_API_KEY || '').trim()) out.push('moonshot');
    if (getNvidiaTextKeys(env).length > 0) out.push('nvidia');
    if (String(env.KIMI_CODE_API_KEY || env.KIMI_API_KEY || '').trim()) out.push('kimi-code');
    return out;
}

/**
 * Write config.toml if the CLI has no credentials yet.
 *
 * Safe to call before every spawn: by default it never clobbers an existing
 * config that already carries a provider (e.g. a real `kimi login` on a dev
 * machine). Pass `{ force: true }` to overwrite regardless — the runtime
 * fail-over uses this to swap a broken provider for the fallback mid-run.
 *
 * @param {object} env
 * @param {{ preference?: string, force?: boolean }} opts
 * @returns {{ ok: boolean, reason: string, configPath?: string, provider?: string }}
 */
export function ensureKimiCliAuth(env = process.env, opts = {}) {
    const { preference = 'auto', force = false } = opts;
    const kimiHome = resolveKimiHome(env);
    const configPath = path.join(kimiHome, 'config.toml');

    // Respect a real human `kimi login` — but NOT our own generated config.
    // Rewriting our config every spawn is deliberate: it lets the NVIDIA key
    // rotate per generation (nextNvidiaTextApiKey advances each call), spreading
    // load across keys' rate limits. A real login has no "Generated by gametok"
    // marker, so we leave it untouched. `force` always overwrites (fail-over).
    if (!force) {
        try {
            const existing = fs.readFileSync(configPath, 'utf-8');
            const isOurs = /Generated by gametok/.test(existing);
            if (/\[providers\./.test(existing) && !isOurs) {
                return { ok: true, reason: 'existing config.toml is a real login — leaving it', configPath, provider: 'existing' };
            }
        } catch {
            // No config yet — that's the case we're here to fix.
        }
    }

    const settings = resolveKimiSettings(env, preference);
    if (!settings) {
        const reason = preference === 'auto'
            ? 'no NVIDIA_API_KEY, MOONSHOT_API_KEY, or KIMI_CODE_API_KEY in environment'
            : `no credentials configured for provider "${preference}"`;
        return { ok: false, reason };
    }

    try {
        fs.mkdirSync(kimiHome, { recursive: true });
        fs.writeFileSync(configPath, buildKimiConfigToml(settings), { mode: 0o600 });
        return {
            ok: true,
            reason: `wrote config.toml (provider "${settings.provider}", type "${settings.providerType}", base_url ${settings.baseUrl}, model ${settings.model})`,
            configPath,
            provider: settings.provider,
        };
    } catch (err) {
        return { ok: false, reason: `failed to write ${configPath}: ${err.message}` };
    }
}
